// operadores de comparacao
const a = 5;
const b = "5";

const igual = a == b; // Igual a (com coerção de tipo)
const estritamenteIgual = a === b; // Estritamente igual a (sem coerção de tipo)
const diferente = a != b; // Diferente de (com coerção de tipo)
const estritamenteDiferente = a !== b; // Estritamente diferente de (sem coerção de tipo)
const maiorQue = a > 3; // Maior que
const menorQue = a < 8; // Menor que
const maiorOuIgual = a >= 5; // Maior ou igual a
const menorOuIgual = a <= 5; // Menor ou igual a
